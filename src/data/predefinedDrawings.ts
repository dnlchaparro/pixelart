// Function to get a random drawing for a specific grid size
export const getRandomDrawing = (gridSize: number): PixelArt | null => {
  const drawings = predefinedDrawings[gridSize];
  if (!drawings || drawings.length === 0) {
    return null;
  }

  const randomIndex = Math.floor(Math.random() * drawings.length);
  return drawings[randomIndex];
};
